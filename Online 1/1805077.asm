; INPUT a OUTPUT Z
; INPUT b OUTPUT X

.MODEL SMALL
.STACK 100H
.DATA
CR EQU 13D
LF EQU 10D
PI DB "ENTER A LETTER BETWEEN A TO M",13,10,"$"
PO DB "THE RESULTANT LETTER IS",13,10,"$"
NL DB " ",13,10,"$"
X DB ?

.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	
	MOV AH, 9
	LEA DX, PI
	INT 21H
	
	MOV AH,1
	INT 21H
	
	MOV X, AL
	
    MOV BL,'a'
    SUB X,BL 
    
    MOV CL,X
    ADD X,CL
    
    MOV BL,'Z'
    SUB BL,X 
    
    MOV AH, 9
	LEA DX, NL
	INT 21H
	MOV AH, 9
	LEA DX, PO
	INT 21H
    
    MOV DL,BL
    MOV AH,2
    INT 21H
    
	MOV AH,4CH
	INT 21H


MAIN ENDP
END MAIN